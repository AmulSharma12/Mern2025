const mongoose = require("mongoose");

//creating user schema
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
  },
  phone: {
    type: String,
    require: true,
  },
  password: {
    type: String,
    require: true,
  },
  isAdmin: {
    type: Boolean,
    default: false,
  },
});

//creaing user model
const User = new mongoose.model("User", userSchema);

//export User model
module.exports = User;
